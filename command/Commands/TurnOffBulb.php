<?php
namespace Commands;

use Commands\Command;
use Receivers\Bulb;

class TurnOffBulb implements Command
{
    protected Bulb $bulb;

    public function __construct(Bulb $bulb)
    {
        $this->bulb = $bulb;
    }

    public function execute()
    {
        $this->bulb->turnOff();
    }

    public function undo()
    {
        $this->bulb->turnOn();
    }

    public function redo()
    {
        $this->execute();
    }


}